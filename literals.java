public class literals {
    public static void main(String[] args) {
        System.out.println("Literals in java");
        byte age = 12;
        int a = 56;
        short b = 78;

        long age2 = 12222222222L;
        // long datatype me last me l or L hoga --> longliterals
        char ch = 'p';
        float f1 = 5.5f; // floatliterals --> f or F are mandatory.
        double d1 = 4.66;
        // by default doubleliterls me d or D phele se hote hai.
        boolean bo = true;
        System.out.println(age);

        /*
         * literals --> a constant value which can be assigned to the variable is called
         * as a literals.
         */

    }
}