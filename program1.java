public class program1 {
     public static void main(String[] args){
        /*
         * import java.util.*; -->libarary
         * scanner --> class
         * new Scanner(System.in)  --> object
         * nextInt() --> scanner class --> function.
         * 
         */

         // arithmetic operators
        System.out.println(2+3);       
        System.out.println(2-3);       
        System.out.println(2*3);     
        System.out.println(2/3);        
        System.out.println(2.0/3);      
        System.out.println(4%3);        
        System.out.println(4.4%3);    
        System.out.println(4.8%1.1);    
        System.out.println();

        // comparison operators        
        System.out.println(2==3);      
        System.out.println(2==2);      
        System.out.println(2>3);        
        System.out.println(2>=3);       
        System.out.println(3<3);        
        System.out.println(3<=3);       

        System.out.println(2!=3);       
        System.out.println();

        // logical operators            
        System.out.println(!true);         
        System.out.println(!false);        


        System.out.println(true && false);  
        System.out.println(2>3 && 3>2);     
        System.out.println(3>2 && 4>2);     



        System.out.println(true || false); 
        System.out.println(2>3 || 3>2);     
        System.out.println(3<2 || 4<2);    
        System.out.println("\n");


        // & bitwise operator (AND bitwise operator)
        System.out.println(2&3);      
      

        // | bitwise operator (OR bitwise operator)
        System.out.println(2|3);     
    
     }
}
